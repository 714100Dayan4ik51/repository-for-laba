a = int(input("Введите первое число - "))
b = int(input("Введиет второе число - "))
print("Сумма чисел - ", (a + b))
print("Разность чисел - ", (a - b))
print("Среднее арифмитическое чисел - ", ((a + b) / 2))
print("Разность максимального и минимального по модулю - ", max(abs(a), abs(b)) - min(abs(a), abs(b)))
print("Частное чисел с точностью до сотых - ", round(a / b, 2))
